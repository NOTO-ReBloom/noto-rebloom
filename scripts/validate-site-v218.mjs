import fs from 'node:fs';
import path from 'node:path';
import vm from 'node:vm';
const root=path.resolve(process.argv[2]||'site');
const failures=[];
for(const f of ['index.html','styles.css','game.js','service-worker.js','site.webmanifest','version.json','data/game-data.js']) if(!fs.existsSync(path.join(root,f))) failures.push(`missing ${f}`);
const html=fs.readFileSync(path.join(root,'index.html'),'utf8');
const css=fs.readFileSync(path.join(root,'styles.css'),'utf8');
const js=fs.readFileSync(path.join(root,'game.js'),'utf8');
const sw=fs.readFileSync(path.join(root,'service-worker.js'),'utf8');
const version=JSON.parse(fs.readFileSync(path.join(root,'version.json'),'utf8'));
const ctx={window:{}};vm.createContext(ctx);vm.runInContext(fs.readFileSync(path.join(root,'data/game-data.js'),'utf8'),ctx);
const d=ctx.window.GAME_DATA;
if(d?.meta?.version!=='2.18.0') failures.push(`version expected 2.18.0 got ${d?.meta?.version}`);
if(version.version!=='2.18.0') failures.push(`version.json expected 2.18.0 got ${version.version}`);
if(d?.nodes?.length!==730) failures.push(`nodes expected 730 got ${d?.nodes?.length}`);
if(Object.keys(d?.evidence||{}).length!==173) failures.push(`evidence expected 173 got ${Object.keys(d?.evidence||{}).length}`);
if(!html.includes('VERSION 2.18.0')) failures.push('visible version missing');
if((html.match(/id="new-game"/g)||[]).length!==1) failures.push('new-game must exist exactly once');
if(!html.includes('id="title-launch-dock"')) failures.push('launch dock missing');
if(!html.includes('id="launch-critical-css"')) failures.push('critical inline launch CSS missing');
if(!html.includes('styles.css?v=2.18.0')||!html.includes('game.js?v=2.18.0')||!html.includes('game-data.js?v=2.18.0')) failures.push('core cache-busting query missing');
const copyEnd=html.indexOf('</div>\n      <div id="title-launch-dock"');
if(copyEnd<0) failures.push('launch dock is not a direct title-screen child after title-copy');
if(!js.includes('window.FB_START = startNewGameFromTitle')) failures.push('public start API missing');
if(!js.includes("get('start') === '1'")) failures.push('direct start query fallback missing');
if(!js.includes("new CustomEvent('fb:ready'")) failures.push('boot health event missing');
if(!css.includes('Final Candidate 2.18')||!css.includes('#title-launch-dock')) failures.push('v218 CSS missing');
if(!sw.includes("fourth-bedroom-v2.18.0")) failures.push('service worker cache version missing');
const refs=[...html.matchAll(/(?:src|href)="([^"]+)"/g),...css.matchAll(/url\(["']?([^"')]+)["']?\)/g),...js.matchAll(/['"](assets\/[^'"]+)['"]/g)].map(m=>m[1]).filter(x=>x&&!x.startsWith('data:')&&!x.startsWith('http')&&!x.startsWith('#'));
for(const r of new Set(refs)){const clean=r.replace(/^\.\//,'').split('?')[0];if((clean.startsWith('assets/')||clean.startsWith('data/'))&&!fs.existsSync(path.join(root,clean))) failures.push(`missing ref ${clean}`);}
const result={version:d?.meta?.version,nodes:d?.nodes?.length,evidence:Object.keys(d?.evidence||{}).length,launchDock:true,criticalCss:true,directStartFallback:true,failures,passed:failures.length===0};
console.log(JSON.stringify(result,null,2));if(failures.length)process.exit(1);
